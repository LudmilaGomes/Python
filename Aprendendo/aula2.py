# inicialização de várias variáveis com o mesmo valor
x = y = z = 0   #tipo inteiro

# Python não é uma linguagem fortemente tipada, o que quer dizer que não precisamos declarar o tipo da variável
# o tipo da variável vai variar de acordo com aquilo que é armazenado na variável
string1 = "Socorro, "   #tipo string
string2 = "meu deus."   #tipo string

print(string1)
print(string2)

# concatenação de strings no 'print'
print(string1 + string2)
print(string1 + " ---- " + string2)